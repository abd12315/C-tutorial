//in this program we are going to do explicit casting for no reason
#include <stdio.h>
#include <stdlib.h>
int main()
{
    float num1=34;
    float num2=(num1)/2;
    printf("this is the original value of the num1:%g\n",num1);
    printf("this is the value of num1 after simplification:%g",num2);
    return 0;

}
//here using name of the variable explicitly for simplification purposes is an example of explicit casting
